# Algorithm:
# 1. Use `math.floor()` to print the largest integer less than or equal to `a`.
# 2. Use `math.gcd()` to find the greatest common divisor of `x` and `y`.
# 3. Use `math.fabs()` to print the absolute value of `b`.

import math

a = 5
print(math.floor(a))  # Floor of 5, which is 5

x, y = 10, 8
print(math.gcd(x, y))  # Greatest common divisor of 10 and 8, which is 2

b = -9
print(math.fabs(b))  # Absolute value of -9, which is 9.0